<h1>Explain setcookie() function in PHP?</h1>

<p>
Definition by php documentation: <br>
setcookie ( string $name [, string $value = "" [, array $options = [] ]] ) : bool
</p>
<p>
The setcookie() function defines a cookie to be sent alongside the remainder of the HTTP headers.<br>
This cookie can be access later through the global variable $_COOKIE;
</p>
